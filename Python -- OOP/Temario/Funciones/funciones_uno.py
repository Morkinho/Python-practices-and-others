def mi_funcion(nombre, apellido, localidad):                                            # En los () se le agregan los parametros.
    print('Saludos desde mi función')
    print(f'Nombre: {nombre}, Apellido: {apellido}, Localidad: {localidad}')

mi_funcion('Daniel', 'Bilbao', 'San Isidro')                                            #Aquí van los argumentos de los parámetros
mi_funcion('Carla', 'Perez', 'Martinez')                                                #Podemos invocar la función tantas veces queramos.
